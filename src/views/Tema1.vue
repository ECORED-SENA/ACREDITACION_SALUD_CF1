    .titulo-segundo.mt-5
      #t_1_1.h4 1.1 Concepto

    p.mt-4 El documento CONPES 3446 de 2006, que define los #[strong “Lineamientos para una Política Nacional de Calidad”] puntualiza como objetivo del #[strong Subsistema Nacional de Calidad], ser un instrumento compuesto por instituciones públicas y privadas que realizan actividades de cualquier orden para la formulación, ejecución y seguimiento de las políticas en materia de normalización, reglamentación técnica, evaluación de la conformidad y metrología.

    p.mt-3 Dicho documento a su vez establece, que el #[strong Sistema Obligatorio de Garantía de Calidad en Salud], SOGCS; se entiende como el conjunto de instituciones, normas, requisitos, mecanismos y procesos deliberados y sistemáticos que desarrollan en el sector para generar, mantener y mejorar la calidad de los servicios de salud en el país.  Este incluye los requisitos mínimos obligatorios para los agentes intervinientes.

    p.mt-3 Gracias a lo establecido en la ley 100 de 1993, bases del Sistema General de Seguridad Social en Salud (SGSSS) plantea la accesibilidad, oportunidad, seguridad, pertinencia y continuidad en salud, para toda la población colombiana con calidad, impulsando la creación del Sistema Obligatorio de la Garantía de la Calidad (SOGCS) con sus 4 componentes, como se presenta en el siguiente gráfico.

